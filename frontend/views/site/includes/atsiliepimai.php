                jQuery(function($) {
                    function generatePages() {
                        var _total, i, _link;

                        _total = $( "#carousel" ).rcarousel( "getTotalPages" );

                        for ( i = 0; i < _total; i++ ) {
                            _link = $( "<a href='#'></a>" );

                            $(_link)
                                .bind("click", {page: i},
                                    function( event ) {
                                        $( "#carousel" ).rcarousel( "goToPage", event.data.page );
                                        event.preventDefault();
                                    }
                                )
                                .addClass( "bullet off" )
                                .appendTo( "#pages" );
                        }

                        // mark first page as active
                        $( "a:eq(0)", "#pages" )
                            .removeClass( "off" )
                            .addClass( "on" )
                            .css( "background-image", "url(/css/img/page-on.png)" );

                    }

                    function pageLoaded( event, data ) {
                        $( "a.on", "#pages" )
                            .removeClass( "on" )
                            .css( "background-image", "url(/css/img/page-off.png)" );

                        $( "a", "#pages" )
                            .eq( data.page )
                            .addClass( "on" )
                            .css( "background-image", "url(/css/img/page-on.png)" );
                    }

                    var s1H = $('#s1').outerHeight() + $('#s2').outerHeight(),
                        s2H = $('#s3').outerHeight() + $('#s4').outerHeight(),
                        wW = $(window).width();

                    if(s1H > s2H){var sH = s1H}else{var sH = s2H}
                    console.log(wW);
                    if(wW < 520){
                        var add = 200;
                    }else if(wW < 975){
                        var add = 100;
                    }else if(wW < 1184){
                        var add = -300;
                    }else if(wW < 1584){
                        var add = -200;
                    }else{
                        var add = -300;
                    }


                    $("#carousel").rcarousel(
                        {
                            visible: 1,
                            step: 1,
                            speed: 700,
                            auto: {
                                enabled: false
                            },
                            width: $('#container').width() - 120,
                            height: sH+add,
                            start: generatePages,
                            pageLoaded: pageLoaded
                        }
                    );



                    $('#ui-carousel-next').add('#ui-carousel-prev').css({height : sH});
                    //$('#ui-carousel-prev').css({height : $('#slide01').height()});

                    $( "#ui-carousel-next" )
